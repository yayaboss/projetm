// -- CREATE
app.post(lienAjouter, function (req, res) {
    let Filiere = mongoose.model('Filiere');
    let newFiliere = new Filiere(req.body);
    newFiliere.id = newFiliere._id;

    newFiliere.save().then(()=>{
        res.redirect(lienAll);
    },(err)=>{
        res.redirect(lienErreur);
    })
});

